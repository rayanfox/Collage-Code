import math

pi = math.pi

rad = float(input("Input the radius: "))

Diameter = 2 * rad
Circumferance = Diameter * pi
Surface_Area = 4 * pi * rad ** 2
Volume = 4/3 * pi * rad ** 3

print("Diameter" ,Diameter)
print("Circumferance" ,Circumferance)
print("Surface Area" ,Surface_Area)
print("Volume" ,Volume)
